						<h1 class="centralizado linha">Apresentação do Sistema Catraca</h1><br>

						<p>O Sistema Catraca - Controle Administrativo de Tráfego Acadêmico Automatizado, é um sistema em desenvolvimento pela DISUP/DTI que atenderá inicialmente as necessidades de controle de acesso no Restaurante Universitário. Oferecendo dentre outras coisas:</p><br>
						
						<ul>
							<li>catracas com uma tela com mensagens, indicadores de luz e sonoridade;</li>
							<li>geração dos relatórios necessários para a gestão,</li>
							<li>cadastro de cartões,</li>
							<li>cadastro de cartões,</li>
							<li>utilização de sistema de créditos,</li>
							<li>Interface para usuário com informações de quantidade de créditos e acessos ao RU.</li>
						</ul><br>
						
						<p>A princípio tentou-se avaliar a opção mais econômica. Pensou-se então em aproveitar as catracas antigas com o seu sistema. No entanto, esta solução não era a mais viável, pois seu código-fonte era proprietário, bem como seu circuito de interação entra a mecânica e o sistema. Tal utilização acarretaria em dependência para com a empresa que fornece essas catracas, bem como sua manutenção e suporte. Por isso foi decidido que seria feita a criação de um sistema próprio completamente voltado para as demandas locais, com circuito próprio e aberto, de baixo custo, fácil manutenção e fácil suporte. Ainda para reduzir os custos foi utilizado o estoque antigo de cartões que estavam guardados. São os cartões que estão atualmente sendo utilizados no cadastro. Possuem a tecnologia RFID(Identificação por Radio Frequência), a mesma utilizada nas carteiras de estudante. O cartão emite um sinal para o leitor, este traduz o seu número para o computador. Durante o cadastro, o usuário tem esse número associado ao seu usuário do SIG. </p><br>

						<p>A pretensão do projeto não é atender somente ao RU, mas oferecer serviços de identificação e controle de tráfego para todo e qualquer setor da UNILAB que estiver interessado. Poderá auxiliar identificação na Biblioteca, ônibus intercampi, controle de entrada nos laboratórios de informática, academia, controle de espaços onde a entrada é restrita, etc. </p><br>

						<p>A entrega do sistema Catraca para utilização no RU está sendo feita em etapas, onde ao concluir um conjunto de módulos do sistema é feito logo em seguida sua implantação, atendendo assim com mais rapidez às necessidades da Comunidade Acadêmica. Toda a implantação será feita utilizando-se paralelamente o sistema manual antigo pelo menos no primeiro momento, por dois motivos: primeiro testar amplamente o sistema, o qual pode conter falhas; segundo para possibilitar uma adaptação tranquila dos usuários à nova situação. </p><br>

						<p>Dessa forma está sendo implantado primeiramente o módulo de cadastro de cartão, identificação e Catraca Virtual. São básicos para o começo da utilização. Nas próximas etapas, no início de Julho, a pretensão do projeto é começar a planejar a implantação de módulos relacionados à recarga de créditos no cartão de usuário e o início de instalação de catracas físicas.</p><br>
						
